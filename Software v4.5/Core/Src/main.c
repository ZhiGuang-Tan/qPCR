/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  static uint8_t DBG_ReceiveBuffer[32];
  int32_t i;
  for(i=0; i<16; i++)
  {
    fluor_handle[i].IsActivated = 1;
    fluor_handle[i].value = -1.0f;
  }
  /* Infinite loop */
  for(;;)
  {
    memset(DBG_ReceiveBuffer, 0x0, 32);
    __HAL_UART_ENABLE_IT(&huart4, UART_IT_IDLE);
    HAL_UART_Receive_DMA(&huart4, DBG_ReceiveBuffer, 0x20);
    xSemaphoreTake(ReceiveBufferBusyBiSemHandle, portMAX_DELAY);
    
    switch(DBG_ReceiveBuffer[2])
    {
      case 0x00: // Stop TEMPC cycle and restart thread.
      {
        // print_uart(&huart4, "0x00\r\n");
        TEMPC_Stop(1);
        TEMPC_Stop(2);
        TEMPC_FAN_NEN = 0;
        if(NULL!=tempcTaskHandle)
        {
          vTaskDelete(tempcTaskHandle);
        }
        tempcTaskHandle = osThreadNew(StartTempcTask, NULL, &tempcTask_attributes);
        break;
      }
      case 0x01: // Set reference and then move to the sampling port.
      {
        if(errQUEUE_FULL==xQueueSend(StepperMessageQueueHandle, DBG_ReceiveBuffer+3, 100))
        {
          print_uart(&huart4, "Stage Queue full!");
        }
        break;
      }
      case 0x02: // Start thermalcycle.
      {
        if(errQUEUE_FULL==xQueueSend(TempcMessageQueueHandle, DBG_ReceiveBuffer+3, 100))
        {
          print_uart(&huart4, "Stage Queue full!");
        }
        break;
      }
      case 0x03: // Query real time data
      {
        switch(DBG_ReceiveBuffer[3])
        {
          case 0x00:
          {
            REALTEMP[0] = TEMPC_GetTemperature(1);
            REALTEMP[1] = TEMPC_GetTemperature(2);
            print_uart(&huart4, "TEM%05.2f_%05.2f\r\n", REALTEMP[0], REALTEMP[1]);
            break;
          }
          case 0x01:
          {
            if(FluorDataReady)
            {
              // print_uart(&huart4, "FLU");
              // HAL_UART_Transmit(&huart4, (uint8_t *)fluor_handle, 16*sizeof(FluorHandle_TypeDef), 50);
              FluorDataReady = 0;
              // print_uart(&huart4, "FLU");
              // for(i=0; i<15; i++)
              // {
              //   print_uart(&huart4, "%f_", fluor_handle[i].value);
              // }
              // print_uart(&huart4, "%f\r\n", fluor_handle[15].value);
              // xSemaphoreGive(FluorSendSuccBiSemHandle); // Continue thermal cycle after fluor data has been sent
            }
            else
            {
              __nop();
              print_uart(&huart4, "NON\r\n");
            }
            break;
          }
        }
        break;
      }
      case 0x04: // Enable channel
      {
        for(i=0; i<16; i++)
        {
          fluor_handle[i].IsActivated = DBG_ReceiveBuffer[i+3];
          fluor_handle[i].value = -1.0f;
        }
      }
      default:
      {
        print_uart(&huart4, "Hello!\r\n");
      }
    }
  }
  /* USER CODE END 5 */
}

